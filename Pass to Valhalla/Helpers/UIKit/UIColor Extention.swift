import UIKit

extension UIColor {
    
    static func themeWhite() -> UIColor {
        return #colorLiteral(red: 0.9882352941, green: 0.9803921569, blue: 0.9843137255, alpha: 1)
    }
    
    static func themeRed() -> UIColor {
        return #colorLiteral(red: 0.6588235294, green: 0, blue: 0.2196078431, alpha: 1)
    }
    
    static func themeBlack() -> UIColor {
        return #colorLiteral(red: 0.168627451, green: 0.1254901961, blue: 0.1411764706, alpha: 1)
    }
}
