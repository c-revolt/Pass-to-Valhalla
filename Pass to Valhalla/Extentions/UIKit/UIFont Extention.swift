import UIKit

extension UIFont {
    
    static func runicFont() -> UIFont? {
        return UIFont.init(name: "Runic", size: 25)
    }
    
    static func runicAltNoFont() -> UIFont? {
        return UIFont.init(name: "Runic AltNo", size: 25)
    }
}
